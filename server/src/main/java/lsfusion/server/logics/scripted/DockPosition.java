package lsfusion.server.logics.scripted;

public final class DockPosition {
    public int x;
    public int y;
    public int width;
    public int height;

    public DockPosition(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
